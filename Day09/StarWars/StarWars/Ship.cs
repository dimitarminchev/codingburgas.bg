public class Ship
{
	private string name;

	public string Name
	{
		get { return name; }
		set { name = value; }
	}

	private int price;

	public int Price
	{
		get { return price; }
		set { price = value; }
	}

	private int resurces;

	public int Resurces
	{
		get { return resurces; }
		set { resurces = value; }
	}

}