namespace Arithmetics
{
    /// <summary>
    /// Аритметични операции
    /// </summary>
    public static class Arithmetics
    {
        /// <summary>
        /// Събиране на две числа
        /// </summary>
        /// <param name="a">Първи операнд</param>
        /// <param name="b">Втори операнд</param>
        /// <returns>Резултат от събирането</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }

        /// <summary>
        /// Умножение на две числа
        /// </summary>
        /// <param name="a">Първи операнд</param>
        /// <param name="b">Втори операнд</param>
        /// <returns>Резултат от умножението</returns>
        public static int Multiply(int a, int b)
        {
            return a * b;
        }
    }
}
